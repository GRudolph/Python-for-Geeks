# mycalc.py with add,subtract, multiply and divide functions

class MyCalc:
    def add(self, x, y):
        """This function adds two numbers"""
        return x + y

    def subtract(self, x, y):
        """This function subtracts two numbers"""
        return x - y

    def multiply(self, x, y):
        """This function subtracts two numbers"""
        return x * y

    def divide(self, x, y):
        """This function subtracts two numbers"""
        return x / y

